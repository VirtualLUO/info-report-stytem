package com.pojo;

public class Group {
	private int id;
	private String groupname;
	
	public int getId() {
		return id;
	}
	public String getGroupname() {
		return groupname;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setGroupname(String groupname) {
		this.groupname = groupname;
	}
}
